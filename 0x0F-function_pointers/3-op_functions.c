#include "3-calc.h"
/**
  * op_add - addition
  * @a: para1
  * @b: para2
  *
  * Return: int
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtraction
  * @a: para1
  * @b: para2
  *
  * Return: int
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplication
  * @a: para1
  * @b: para2
  *
  * Return: int
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - division
  * @a: para1
  * @b: para2
  *
  * Return: int
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - modulus
  * @a: para1
  * @b: para2
  *
  * Return: int
  */
int op_mod(int a, int b)
{
	return (a % b);
}
